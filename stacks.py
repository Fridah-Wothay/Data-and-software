fruits = []
fruits.append("Guava")
fruits.append("oranges")
fruits.append("Kiwi")
print(fruits)

# accessing stack
fruits=['Guava','oranges','Kiwi']
print(fruits[2])

# adding items in stack
fruits=['Guava','oranges','Kiwi']
fruits.append('ovacado')
print(fruits)

#remove items in stack
fruits=['Guava','oranges','Kiwi','ovacado']
fruits.pop()
print(fruits)